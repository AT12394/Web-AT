			tersebut sangat penting untuk menjaga kinerjanya dan mencegah masalah yang
			mungkin timbul. Berikut adalah beberapa alasan mengapa AC membutuhkan perawatan:
		</p>
		<ol>
			<li>
				<h3>Mencegah Penumpukan Debu dan Kotoran</h3>
				<p>
					AC memiliki komponen seperti filter udara dan evaporator yang rentan
					terhadap penumpukan debu dan kotoran. Perawatan rutin termasuk pembersihan
					filter dan komponen lainnya untuk menjaga aliran udara yang baik.
				</p>
			</li>
			<li>
				<h3>Optimalkan Efisiensi Energi</h3>
				<p>
					AC yang terawat dengan baik cenderung lebih efisien dalam penggunaan energi.
					Membersihkan dan merawat komponen seperti kondensor dan evaporator membantu 
					menjaga efisiensi energi perangkat.
				</p>
			</li>
			<li>
				<h3>Pencegahan Kelembaban Berlebih</h3>
				<p>
					AC tidak hanya mengatur suhu tetapi juga mengatasi kelembaban udara.
					Perawatan melibatkan pemeriksaan dan penyesuaian kelembaban untuk mencegah
					pertumbuhan jamur dan jamur yang dapat merugikan kesehatan.
				</p>
			</li>
			<li>
				<h3>Mencegah Kebocoran Refrigeran</h3>
				<p>
					Refrigeran adalah komponen kunci dalam kinerja AC. Perawatan melibatkan
					pemeriksaan berkala untuk mendeteksi dan memperbaiki potensi kebocoran 
					refrigeran yang dapat mengurangi efisiensi sistem.
				</p>
			</li>
			<li>
				<h3>Pembersihan Saluran Pembuangan Air</h3>
				<p>
					Saluran pembuangan air AC dapat tersumbat oleh kotoran dan bakteri. 
					Perawatan mencakup pembersihan saluran pembuangan untuk mencegah 
					genangan air yang dapat menyebabkan masalah kelembaban dan bau tidak sedap.
				</p>
			</li>
			<li>
				<h3>Pemantauan Kondisi Motor dan Kipas</h3>
				<p>
					Motor dan kipas pada AC dapat mengalami keausan. Perawatan melibatkan
					pemantauan kondisi kipas dan motor serta pelumasan yang diperlukan untuk
					menjaga agar AC beroperasi dengan lancar.
				</p>
			</li>
			<li>
				<h3>Meningkatkan Kualitas Udara dalam Ruangan</h3>
				<p>
					Filter udara yang kotor dapat menyebabkan penurunan kualitas udara dalam
					ruangan. Melakukan perawatan pada filter udara membantu menjaga udara bersih
					dan bebas dari partikel berbahaya.
				</p>
			</li>
			<li>
				<h3>Pencegahan Masalah Pendingin</h3>
